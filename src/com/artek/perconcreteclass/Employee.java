package com.artek.perconcreteclass;

public class Employee {
	
	private Integer emplId;
	private String name;
	
	public Integer getEmplId() {
		return emplId;
	}
	public void setEmplId(Integer emplId) {
		this.emplId = emplId;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}		
	
}
